// Get all questions in a flat array
const getAllQuestions = () => {
    const years = ["Jaar 1", "Jaar 2", "Jaar 3", "Jaar 4"];
    const allQuestions = [];
    
    years.forEach(year => {
        data[year].forEach(themeQuestions => {
            themeQuestions.forEach(question => {
                allQuestions.push(question);
            });
        });
    });
    
    return allQuestions;
};

// Function to get a random question
const getRandomQuestion = (questions) => {
    const randomIndex = Math.floor(Math.random() * questions.length);
    return questions[randomIndex];
};

// Function to update the question with fade effect
const updateQuestion = (questionElement, questions) => {
    // Fade out
    questionElement.style.opacity = '0';
    
    // Wait for fade out, then change text and fade in
    setTimeout(() => {
        questionElement.textContent = getRandomQuestion(questions);
        questionElement.style.opacity = '1';
    }, 1000);
};

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    const questionElement = document.getElementById('random-question');
    const allQuestions = getAllQuestions();
    
    // Set initial question
    questionElement.textContent = getRandomQuestion(allQuestions);
    
    // Update question every 5 seconds
    setInterval(() => {
        updateQuestion(questionElement, allQuestions);
    }, 5000);

    // Add background image to the section
    const section = document.getElementById('onderzoeks-themas');
    section.style.backgroundImage = 'url("images/global-challenges.png")';
});

// Populate the table body
const tbody = document.getElementById("data-table-body");

data["Maatschappelijk thema"].forEach((theme, index) => {
    const row = document.createElement("tr");
    row.classList.add("bg-white", "hover:bg-gray-100");

    // Add the theme cell
    const themeCell = document.createElement("td");
    themeCell.classList.add("border", "border-gray-300", "px-4", "py-2", "font-semibold");
    themeCell.textContent = theme;
    row.appendChild(themeCell);

    // Add data for each year
    ["Jaar 1", "Jaar 2", "Jaar 3", "Jaar 4"].forEach(year => {
        const cell = document.createElement("td");
        cell.classList.add("border", "border-gray-300", "px-4", "py-2");

        // Convert array of questions into a list
        const questions = data[year][index];
        const list = document.createElement("ul");
        questions.slice(0, 2).forEach(question => {
            const listItem = document.createElement("li");
            listItem.classList.add("flex", "items-center", "space-x-2", "my-1.5");

            // Add magnifying glass icon
            const icon = document.createElement("i");
            icon.classList.add("fas", "fa-search", "text-gray-500");

            // Add question text
            const text = document.createElement("span");
            text.textContent = question;

            listItem.appendChild(icon);
            listItem.appendChild(text);
            list.appendChild(listItem);
        });

        cell.appendChild(list);
        row.appendChild(cell);
    });

    tbody.appendChild(row);
});